v <- c(2, 5.5, 6, 9)
t <- c(8, 3, 4, 9)

# Arithmetic Operators
v + t
v - t
v * t
v/t
v%%t  # remainder of division
v%/%t  # int part of division
v^t  # pow(v1, v2)

# Relational Operators
v > t
v < t
v == t
v <= t
v >= t
v != t

# Logical Operators, All numbers greater than 1 are considered as logical value
# TRUE
v & t
v | t
!v
v && t  # take first element
v || t  # take first element

